function generateHeatmapPoints(predictionData) {
    const points = [];
    // predictionData ê¸°ë°˜ ì‹œë“œ ìƒì„± (ê°™ì€ ë°ì´í„° â†’ ê°™ì€ ë³€ì´)
    const codes = Object.keys(predictionData).sort();
    const dataSeed = hashCode(codes.map(c => `${c}:${predictionData[c].riskLevel}`).join(','));

    SUB_REGIONAL_POINTS.forEach((sp, idx) => {
        const provData = predictionData[sp.province];
        if (provData) {
            const variation = (seededRandom(dataSeed + idx * 31) - 0.5) * 0.8;
            const value = Math.max(0, Math.min(4, provData.riskLevel + variation));
            points.push({
                lon: sp.lon,
                lat: sp.lat,
                value: value
            });
        }
    });
    return points;
}

